export function getDictionary(language: LanguageCode) {
  return translations[language];
}

export function getStageLabel(
  dictionary: ReturnType<typeof getDictionary>,
  value: ReadingStage | null,
) {
  if (!value) return "-";
  return {
    foundation: "foundation" in dictionary ? undefined : undefined,
  } as never;
}

export function labelForStage(language: LanguageCode, value: ReadingStage | null) {
  if (!value) return "-";
  return language === "zh-CN"
    ? {
        foundation: "基础",
        core: "核心",
        frontier: "前沿",
        update: "更新",
      }[value]
    : {
        foundation: "Foundation",
        core: "Core",
        frontier: "Frontier",
        update: "Update",
      }[value];
}

